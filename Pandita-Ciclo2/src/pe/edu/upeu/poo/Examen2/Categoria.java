package pe.edu.upeu.poo.Examen2;

public class Categoria {
	private String nombrado;
	private String trabajadorIndependiente;
	private String empleado;
	private String contratado;
	public String getNombrado() {
		return nombrado;
	}
	public void setNombrado(String nombrado) {
		this.nombrado = nombrado;
	}
	public String getTrabajadorIndependiente() {
		return trabajadorIndependiente;
	}
	public void setTrabajadorIndependiente(String trabajadorIndependiente) {
		this.trabajadorIndependiente = trabajadorIndependiente;
	}
	public String getEmpleado() {
		return empleado;
	}
	public void setEmpleado(String empleado) {
		this.empleado = empleado;
	}
	public String getContratado() {
		return contratado;
	}
	public void setContratado(String contratado) {
		this.contratado = contratado;
	}
	
	

}
