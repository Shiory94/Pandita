package pe.edu.upeu.poo.Examen2;

public class Especialidad {
	private String ingeniero;
	private String licenciado;
	private String programador;
	private String doctor;
	private String enfermera;
	private String psicologico;
	private String obrero;
	public String getIngeniero() {
		return ingeniero;
	}
	public void setIngeniero(String ingeniero) {
		this.ingeniero = ingeniero;
	}
	public String getLicenciado() {
		return licenciado;
	}
	public void setLicenciado(String licenciado) {
		this.licenciado = licenciado;
	}
	public String getProgramador() {
		return programador;
	}
	public void setProgramador(String programador) {
		this.programador = programador;
	}
	public String getDoctor() {
		return doctor;
	}
	public void setDoctor(String doctor) {
		this.doctor = doctor;
	}
	public String getEnfermera() {
		return enfermera;
	}
	public void setEnfermera(String enfermera) {
		this.enfermera = enfermera;
	}
	public String getPsicologico() {
		return psicologico;
	}
	public void setPsicologico(String psicologico) {
		this.psicologico = psicologico;
	}
	public String getObrero() {
		return obrero;
	}
	public void setObrero(String obrero) {
		this.obrero = obrero;
	}
	

}
