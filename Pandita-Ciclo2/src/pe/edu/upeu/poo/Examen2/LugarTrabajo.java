package pe.edu.upeu.poo.Examen2;

public class LugarTrabajo {
	private String salones;
	private String campo;
	private String centroMedico;
	private String direccion;
	private String construccion;
	public String getSalones() {
		return salones;
	}
	public void setSalones(String salones) {
		this.salones = salones;
	}
	public String getCampo() {
		return campo;
	}
	public void setCampo(String campo) {
		this.campo = campo;
	}
	public String getCentroMedico() {
		return centroMedico;
	}
	public void setCentroMedico(String centroMedico) {
		this.centroMedico = centroMedico;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getConstruccion() {
		return construccion;
	}
	public void setConstruccion(String construccion) {
		this.construccion = construccion;
	}
	
	

}
