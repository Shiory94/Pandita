package pe.edu.upeu.poo.modelomatricula;

public class EstadoCivil {
	private String SOltero, casado , viudo, divorciado, convivinte;

	public String getSOltero() {
		return SOltero;
	}

	public void setSOltero(String sOltero) {
		SOltero = sOltero;
	}

	public String getCasado() {
		return casado;
	}

	public void setCasado(String casado) {
		this.casado = casado;
	}

	public String getViudo() {
		return viudo;
	}

	public void setViudo(String viudo) {
		this.viudo = viudo;
	}

	public String getDivorciado() {
		return divorciado;
	}

	public void setDivorciado(String divorciado) {
		this.divorciado = divorciado;
	}

	public String getConvivinte() {
		return convivinte;
	}

	public void setConvivinte(String convivinte) {
		this.convivinte = convivinte;
	}
	

}
